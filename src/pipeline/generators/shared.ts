export const GENERATED_MARKER = '<!-- Generated by AgentFlow. Manual changes may be overwritten. -->';

export function list(items: string[] | undefined, fallback = 'None.'): string {
  if (!items || items.length === 0) return fallback;
  return items.map((item) => `- ${item}`).join('\n');
}

export function frontmatterValue(value: string): string {
  return value.replace(/\n/g, ' ').replace(/"/g, '\\"');
}

export function yamlList(name: string, items: string[] | undefined): string {
  if (!items || items.length === 0) return `${name}: []`;
  return `${name}:\n${items.map((item) => `  - ${item}`).join('\n')}`;
}

export function ensureTrailingNewline(content: string): string {
  return content.endsWith('\n') ? content : `${content}\n`;
}
